    private void inicializarTableModel() {
        String[] cabecera = {"ID", "Detalle", "Dentista", "Paciente", "Tipo", "Fecha", "Hora Inicio", "Hora Fin", "Precio"};
        tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(cabecera);
        tbCitas.setModel(tableModel);
        listaCitas = new ArrayList<>();
    }

    private void setDatos() {
        Object[] fila = new Object[tableModel.getColumnCount()];
        tableModel.setRowCount(0);
        for (Cita cita : listaCitas) {
            fila[0] = cita.getIdcita();
            fila[1] = cita.getDetallecita();
            fila[2] = cita.getDentista().getNombres();
            fila[3] = cita.getPaciente().getNombres();
            fila[4] = cita.getTipocita();
            fila[5] = cita.getFecha();
            fila[6] = cita.getHorarioatencion().getHoraInicio();
            fila[7] = cita.getHorarioatencion().getHoraFin();
            fila[8] = cita.getPagototal();
            tableModel.addRow(fila);
        }

    }

