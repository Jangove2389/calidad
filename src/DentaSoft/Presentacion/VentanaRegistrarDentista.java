    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            capturarDatosDeDentista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR AL INGRESAR DATOS", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            guardarDentista();
            inicializarNuevoDentista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void guardarDentista() throws Exception {
        registrarDentistaServicio.guardarDentista(dentista);
        JOptionPane.showMessageDialog(this, "SE GUARDO EL DENTISTA EXITOSAMENTE", "Información", JOptionPane.INFORMATION_MESSAGE);
    }

    private void capturarDatosDeDentista() throws NumberFormatException {
        dentista.setNombres(txtNombres.getText().trim());
        dentista.setDni(Integer.parseInt(txtDni.getText().trim()));
        dentista.setEdad(Integer.parseInt(txtEdad.getText().trim()));
        dentista.setEmail(txtEmail.getText().trim());
        dentista.setTelefono(txtTelefono.getText().trim());
        dentista.setCantidadcitas(Integer.parseInt(txtCantcitas.getText().trim()));
        dentista.setTiempotolerancia(Integer.parseInt(txtTimetolerancia.getText().trim()));
    }

